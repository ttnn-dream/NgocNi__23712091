a=int(input("Nhập vào số nguyên a: "))
b=int(input("Nhập vào số nguyên b: "))
c=int(input("Nhập vào số nguyên c: "))
solonnhat=max(a, b, c)
print("Số lớn nhất là: ",solonnhat)
sobenhat=min(a, b, c)
print("Số bé nhất là: ",sobenhat)