a=int(input("Nhập số nguyên a: "))
b=int(input("Nhập số nguyên b: "))
#Tính tổng
tong=a+b
print("Tổng hai số là: ",tong)
#Tính hiệu
hieu=a-b
print("Hiệu hai số là: ",hieu)
#Tính tích
tich=a*b
print("Tích hai số là: ",tich)
#Tính thương
thuong=a/b
print("Thương được làm tròn 2 chữ số là: ",round(thuong,2))
print("Thương được làm tròn 3 chữ số là: ",round(thuong,3))
