a=int(input("Nhập a: "))
b=int(input("Nhập b: "))
#Tính tổng
tong=a+b
print("Tổng hai số là: ",tong)
#Hiệu
hieu=a-b
print("Hiệu hai số là: ",hieu)
#Tích
tich=a*b
print("Tích hai số là: ",tich)
#Thương
thuong=a/b
print("Thương hai số là: ",thuong)
tron2chuso=round(thuong,2)
tron3chuso=round(thuong,3)
print("Làm tròn 2 chữ số là: ",tron2chuso)
print("Làm tròn 3 chữ số là: ",tron3chuso)
#Chia lấy dư
cld=a%b
print("Chia lấy dư 2 số là: ",cld)
cln=a//b
print("Chia lấy nguyên 2 số là: ",cln)
